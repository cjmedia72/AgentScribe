export function exportPlaywright(session) {
  const lines = [
    `// AUTO-GENERATED by AgentScribe`,
    `// Session: ${session.id} | Recorded: ${new Date(session.startTime).toISOString()}`,
    `// URL: ${session.startUrl}`,
    `// Events: ${session.events.length} DOM | ${session.networkEvents.length} Network`,
    ``,
    `const { chromium } = require('playwright');`,
    ``,
    `(async () => {`,
    `  const browser = await chromium.launch({ headless: false });`,
    `  const context = await browser.newContext();`,
    `  const page = await context.newPage();`,
    ``
  ];

  const actionEvents = session.events.filter(e =>
    ['click', 'input', 'navigation', 'scroll'].includes(e.type)
  );

  actionEvents.forEach((event, i) => {
    lines.push(`  // Step ${i + 1} — ${event.type} @ ${formatTime(event.timestamp - session.startTime)}`);

    if (event.type === 'navigation') {
      lines.push(`  await page.goto(${quote(event.url)});`);
    } else if (event.type === 'click') {
      const sel = bestSelector(event.element);
      lines.push(`  await page.click(${quote(sel)});`);
      if (event.triggeredRequests?.length) {
        event.triggeredRequests.forEach(req => {
          lines.push(`  // -> ${req.method} ${truncate(req.url, 100)} [${req.responseStatus}]`);
          if (req.postData) {
            const payload = typeof req.postData === 'string'
              ? req.postData
              : JSON.stringify(req.postData);
            lines.push(`  //    payload: ${truncate(payload, 120)}`);
          }
        });
      }
    } else if (event.type === 'input') {
      const sel = bestSelector(event.element);
      const val = event.value === '[REDACTED]' ? 'YOUR_VALUE_HERE' : event.value;
      lines.push(`  await page.fill(${quote(sel)}, ${quote(val || '')});`);
      if (event.value === '[REDACTED]') {
        lines.push(`  // ^ SENSITIVE FIELD — replace YOUR_VALUE_HERE with actual value`);
      }
      if (event.flag === 'INJECTABLE_POINT') {
        lines.push(`  // ^ INJECTABLE POINT — POST param: ${event.element?.name || event.element?.id || '?'}`);
      }
    } else if (event.type === 'scroll') {
      lines.push(`  await page.evaluate(() => window.scrollTo(${event.scrollX || 0}, ${event.scrollY || 0}));`);
    }

    lines.push(``);
  });

  lines.push(`  await browser.close();`);
  lines.push(`})();`);

  const content = lines.join('\n');
  const blob = new Blob([content], { type: 'application/javascript' });
  const filename = `agentscribe-playwright-${Date.now()}.js`;
  downloadBlob(blob, filename);
  return { filename, size: blob.size };
}

function bestSelector(element) {
  if (!element) return 'body';
  if (element.id) {
    // IDs with CSS-special chars need attribute-selector form.
    if (/[^a-zA-Z0-9_-]/.test(element.id)) {
      return `[id="${element.id.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"]`;
    }
    return `#${element.id}`;
  }
  if (element.ariaLabel) {
    return `[aria-label="${element.ariaLabel.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"]`;
  }
  if (element.cssSelector) return element.cssSelector;
  if (element.xpath) return `xpath=${element.xpath}`;
  return element.tag || 'body';
}

function quote(s) {
  return `'${String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

function truncate(s, max) {
  if (!s) return '';
  return s.length > max ? s.slice(0, max) + '...' : s;
}

function formatTime(ms) {
  const s = Math.floor(ms / 1000);
  const m = Math.floor(s / 60);
  return `${m}:${String(s % 60).padStart(2, '0')}`;
}

function downloadBlob(blob, filename) {
  const url = URL.createObjectURL(blob);
  chrome.downloads.download({ url, filename, saveAs: true }, () => {
    URL.revokeObjectURL(url);
  });
}
